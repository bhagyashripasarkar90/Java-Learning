package JavaBasics;

public class LoopsConcepts {

	public static void main(String[] args) {
		
		
		//1. While loop
		int i = 0;
		while (i<=10)
		{
			System.out.println("While loop:"+i);
			i = i+1;
		}
		
		//2. for loop
		for (int j=1;j<100;j++)
		{
			System.out.println("For Loop:"+j);
		}
		
		//3. for loop for 10 to 1
		int k = 10;
		for(k=10;k>=-10;k--)
		{
			System.out.println(k);
		}

	}

}
